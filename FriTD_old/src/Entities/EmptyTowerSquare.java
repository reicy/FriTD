package Entities;

public class EmptyTowerSquare {

	
	private int x, y, id;

	public EmptyTowerSquare(int x, int y, int id) {
		super();
		this.x = x;
		this.y = y;
		this.id = id;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getId() {
		return id;
	}
	
	
	
	
}
